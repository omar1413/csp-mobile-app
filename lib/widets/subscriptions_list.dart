import 'package:csp_mobile_app/models/subscriptions_data.dart';
import 'package:csp_mobile_app/widets/subscripe_item.dart';
import 'package:flutter/material.dart';
import '../models/transaction_data.dart';
import './transaction_item.dart';

class SubscriptionList extends StatelessWidget {
  @override
  final List<SubscripeData> subscriptionList;
  SubscriptionList(this.subscriptionList);
  @override
  Widget build(BuildContext context) {
    return ListView.builder(
      itemBuilder: (ctx, index) {
        return SubscripeItem(
          item: subscriptionList[index],
        );
      },
      itemCount: subscriptionList.length,
    );
  }
}
